    @Override
    public void commit() throws SQLException {
        System.out.println("commit");

        //等待
        new Thread(new Runnable(){
            @Override
            public void run() {
                txTransactional.getTask().waitTask();

                try {
                    if(txTransactional.getTransactionType().equals(TransactionType.commit)){
                        connection.commit();
                    }else{
                        connection.rollback();
                    }
                    connection.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }).start();

    }

